def _resolve_claude_bin() -> str:
    bin_path = shutil.which("claude")
    if bin_path:
        return bin_path

    candidates: list[str] = ["/usr/local/bin/claude", "/opt/homebrew/bin/claude"]

    nvm_node_dir = os.path.expanduser("~/.nvm/versions/node")
    if os.path.isdir(nvm_node_dir):
        for ver in sorted(os.listdir(nvm_node_dir), reverse=True):
            candidates.append(os.path.join(nvm_node_dir, ver, "bin", "claude"))

    diag = []
    for p in candidates:
        e = os.path.exists(p)
        x = os.access(p, os.X_OK) if e else False
        diag.append(f"  {p}: exists={e}, x_ok={x}")
        if e and x:
            return p

    raise FileNotFoundError(
        "`claude` binary not found. shutil.which returned None.\n"
        f"Candidates checked:\n" + "\n".join(diag) + "\n"
        f"PATH={os.environ.get('PATH', '<unset>')}\n"
        f"HOME={os.environ.get('HOME', '<unset>')}"
    )


_CLAUDE_BIN: str | None = None


def _claude_bin() -> str:
    global _CLAUDE_BIN
    if _CLAUDE_BIN is None:
        _CLAUDE_BIN = _resolve_claude_bin()
    return _CLAUDE_BIN


