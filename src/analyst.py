def chat(user_message: str, system_prompt: str | None = None) -> str:
    """Free-form natural language reply for the bot. Optional custom system prompt."""
    env = os.environ.copy()
    env.pop("ANTHROPIC_API_KEY", None)
    env.pop("ANTHROPIC_AUTH_TOKEN", None)

    sys_p = system_prompt or CHAT_SYSTEM_PROMPT
    now_str = datetime.now(KST).strftime("%Y-%m-%d %A %H:%M KST")
    full_prompt = (
        f"{sys_p}\n\n"
        f"---\n\n"
        f"분석 시점: {now_str}\n"
        f"(중요: web_search 결과 기사 날짜를 위 분석 시점과 비교하세요. "
        f"분석 시점에서 2일 초과 지난 기사는 '현재 상황'으로 단정하지 말고 시점을 명시하세요.)\n\n"
        f"사용자 메시지:\n{user_message}\n\n"
        f"위 메시지에 시스템 가이드대로 답변하세요. 본문만 출력."
    )
    model_alias = config.CLAUDE_MODEL or "opus"

    logger.info("Calling claude -p (chat) prompt_chars=%d", len(full_prompt))
    last_err: Exception | None = None
    for attempt in (1, 2):
        try:
            result = subprocess.run(
                [_claude_bin(), "-p", "--model", model_alias],
                input=full_prompt,
                capture_output=True,
                text=True,
                timeout=300,
                env=env,
            )
            break
        except subprocess.TimeoutExpired as e:
            last_err = e
            logger.warning("chat claude -p timed out (attempt %d/2). Retrying...", attempt)
            import time
            time.sleep(5)
    else:
        raise RuntimeError(f"chat claude -p kept timing out: {last_err}")

    if result.returncode != 0:
        raise RuntimeError(f"chat claude -p failed (exit {result.returncode}): {result.stderr.strip()}")
    text_blocks = [b.strip() for b in result.stdout.split("\n") if b.strip()]
    return "\n".join(text_blocks).strip() or "죄송합니다. 답변을 생성하지 못했습니다."


def analyze(market: str, snapshot: dict, user_note: str | None = None) -> str:
    prompt = _build_prompt(market, snapshot, user_note=user_note)
    model_alias = config.CLAUDE_MODEL or "opus"

    # Force Claude Code to use the logged-in OAuth session, not an external API key.
    # A stale/invalid ANTHROPIC_API_KEY leaking in from .env or shell rc would
    # otherwise make `claude -p` fail with "Invalid API key".
    env = os.environ.copy()
    env.pop("ANTHROPIC_API_KEY", None)
    env.pop("ANTHROPIC_AUTH_TOKEN", None)

    logger.info("Calling claude -p with model=%s, prompt_chars=%d", model_alias, len(prompt))
    # Wake-from-sleep can leave the first call unstable: timeout, exit≠0, or empty stdout.
    # Try up to 3 times. Each timeout is 15 min (web_search can be slow).
    import time as _time
    last_err: Exception | str = ""
    result = None
    for attempt in (1, 2, 3):
        try:
            result = subprocess.run(
                [_claude_bin(), "-p", "--model", model_alias],
                input=prompt,
                capture_output=True,
                text=True,
                timeout=900,
                env=env,
            )
            if result.returncode == 0 and result.stdout.strip():
                break  # success
            # Bad result: log and retry
            last_err = (
                f"exit={result.returncode} "
                f"stdout_len={len(result.stdout)} "
                f"stderr={result.stderr[:200]!r}"
            )
            logger.warning("claude -p bad result (attempt %d/3): %s", attempt, last_err)
        except subprocess.TimeoutExpired as e:
            last_err = f"TimeoutExpired after {e.timeout}s"
            logger.warning("claude -p timed out (attempt %d/3): %s", attempt, last_err)
        if attempt < 3:
            _time.sleep(15)
    else:
        # All attempts exhausted (loop did not break out via success)
        raise RuntimeError(f"claude -p kept failing after 3 attempts: {last_err}")

    return result.stdout.strip()
